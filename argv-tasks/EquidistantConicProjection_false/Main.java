import org.sosy_lab.sv_benchmarks.Verifier;

/** The Equidistant Conic projection. */
public class Main {

  private double standardLatitude1 = Verifier.nondetDouble();
  private double standardLatitude2 = Verifier.nondetDouble();

  private double eccentricity = 0.822719;
  private double eccentricity2 = eccentricity * eccentricity;
  private double radius = 1;

  private boolean northPole = Verifier.nondetBoolean();
  private double f = Verifier.nondetDouble(),
      n = Verifier.nondetDouble(),
      rho0 = Verifier.nondetDouble();

  /** ARG-V: suitable */
  private void initialize(
      double rlong0, double rlat0, double standardLatitude1, double standardLatitude2) {
    double rho0 = Verifier.nondetDouble();
    double n = Verifier.nondetDouble();
    double t_rlat0 = Verifier.nondetDouble();

    northPole = rlat0 > 0.0;
    double t_standardLatitude1 = Verifier.nondetDouble();
    double m_standardLatitude1 =
        Math.cos(standardLatitude1)
            / Math.sqrt(1.0 - eccentricity2 * Math.pow(Math.sin(standardLatitude1), 2.0));
    double t_standardLatitude2 = Verifier.nondetDouble();
    double m_standardLatitude2 =
        Math.cos(standardLatitude2)
            / Math.sqrt(1.0 - eccentricity2 * Math.pow(Math.sin(standardLatitude2), 2.0));
    t_rlat0 = Verifier.nondetDouble();

    if (standardLatitude1 != standardLatitude2) {
      n =
          (Math.log(m_standardLatitude1) - Math.log(m_standardLatitude2))
              / (Math.log(t_standardLatitude1) - Math.log(t_standardLatitude2));
      assert Math.sin(standardLatitude1) != Math.sin(standardLatitude2);
    } else {
      n = Math.sin(standardLatitude1);
    }

    f = m_standardLatitude1 / (n * Math.pow(t_standardLatitude1, n));
    rho0 = radius * f * Math.pow(t_rlat0, n);
  }

  /** This main was generated by ARG-V */
  public static void main(String[] args) throws Exception {
    Main instance = new Main();
    instance.initialize(
        Verifier.nondetDouble(),
        Verifier.nondetDouble(),
        Verifier.nondetDouble() * Verifier.nondetInt(),
        Verifier.nondetDouble() * Verifier.nondetInt());
  }
}
