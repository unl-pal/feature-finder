import org.sosy_lab.sv_benchmarks.Verifier;

/** Holds An PieChart Segment */
/** filtered by ARG-V */
public class Main {
  private float mStartAngle = Verifier.nondetFloat();

  private float mEndAngle = Verifier.nondetFloat();

  /**
   * Checks if angle falls in segment.
   *
   * @param angle
   * @return true if in segment, false otherwise.
   */
  /** ARG-V: suitable */
  public boolean isInSegment(double angle) {
    if (angle >= mStartAngle && angle <= mEndAngle) {
      return true;
    }
    double cAngle = angle % 360;
    double startAngle = mStartAngle;
    double stopAngle = mEndAngle;
    while (stopAngle > 360) {
      startAngle -= 360;
      stopAngle -= 360;
    }
    assert cAngle >= -180.0 && cAngle <= 180.0; // inline assert generated by ARG-V
    return cAngle >= startAngle && cAngle <= stopAngle;
  }

  /** This main was generated by ARG-V */
  public static void main(String[] args) throws Exception {
    Main instance = new Main();
    instance.isInSegment(Verifier.nondetDouble() * Verifier.nondetInt());
  }
}
