import org.sosy_lab.sv_benchmarks.Verifier;

/** An abstract class to be implemented by the chart rendering classes. */
public abstract class Main {
  /** ARG-V: suitable */
  private static float[] calculateDrawPoints(
      float p1x, float p1y, float p2x, float p2y, int screenHeight, int screenWidth) {
    float drawP1x = Verifier.nondetFloat();
    float drawP1y = Verifier.nondetFloat();
    float drawP2x = Verifier.nondetFloat();
    float drawP2y = Verifier.nondetFloat();

    if (p1y > screenHeight) {
      // Intersection with the top of the screen
      float m = (p2y - p1y) / (p2x - p1x);
      drawP1x = (screenHeight - p1y + m * p1x) / m;
      drawP1y = screenHeight;

      if (drawP1x < 0) {
        // If Intersection is left of the screen we calculate the intersection
        // with the left border
        drawP1x = 0;
        drawP1y = p1y - m * p1x;
      } else if (drawP1x > screenWidth) {
        // If Intersection is right of the screen we calculate the intersection
        // with the right border
        drawP1x = screenWidth;
        drawP1y = m * screenWidth + p1y - m * p1x;
      }
    } else if (p1y < 0) {
      float m = (p2y - p1y) / (p2x - p1x);
      drawP1x = (-p1y + m * p1x) / m;
      drawP1y = 0;
      if (drawP1x < 0) {
        drawP1x = 0;
        drawP1y = p1y - m * p1x;
      } else if (drawP1x > screenWidth) {
        drawP1x = screenWidth;
        drawP1y = m * screenWidth + p1y - m * p1x;
      }
    } else {
      // If the point is in the screen use it
      drawP1x = p1x;
      drawP1y = p1y;
    }

    if (p2y > screenHeight) {
      // Added cast to introduce potential of ArithmeticException
      float m = ((int) (p2y - p1y)) / ((int) (p2x - p1x));
      drawP2x = (screenHeight - p1y + m * p1x) / m;
      drawP2y = screenHeight;
      if (drawP2x < 0) {
        drawP2x = 0;
        drawP2y = p1y - m * p1x;
      } else if (drawP2x > screenWidth) {
        drawP2x = screenWidth;
        drawP2y = m * screenWidth + p1y - m * p1x;
      }
    } else if (p2y < 0) {
      float m = (p2y - p1y) / (p2x - p1x);
      drawP2x = (-p1y + m * p1x) / m;
      drawP2y = 0;
      if (drawP2x < 0) {
        drawP2x = 0;
        drawP2y = p1y - m * p1x;
      } else if (drawP2x > screenWidth) {
        drawP2x = screenWidth;
        drawP2y = m * screenWidth + p1y - m * p1x;
      }
    } else {
      // If the point is in the screen use it
      drawP2x = p2x;
      drawP2y = p2y;
    }

    return new float[] {drawP1x, drawP1y, drawP2x, drawP2y};
  }

  /** This main was generated by ARG-V */
  public static void main(String[] args) throws Exception {
    calculateDrawPoints(
        Verifier.nondetFloat(),
        Verifier.nondetFloat(),
        Verifier.nondetFloat(),
        Verifier.nondetFloat(),
        Verifier.nondetInt(),
        Verifier.nondetInt());
  }
}
