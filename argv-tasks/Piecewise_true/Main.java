import org.sosy_lab.sv_benchmarks.Verifier;

public class Main {

  double previousEvent = 0;

  double values[] = new double[] {Verifier.nondetDouble()};
  double intervals[] = new double[] {Verifier.nondetDouble(), Verifier.nondetDouble()};

  /** ARG-V: suitable */
  public double generateNextEvent(double currentTime) {
    float index = Verifier.nondetFloat();
    float range = Verifier.nondetFloat();

    int i = 0;
    for (i = 0; i < values.length; i++) {
      if (index >= intervals[i] && index < intervals[i + 1]) break;
    }
    assert i <= values.length;
    if (i == 0) i++;
    double nextEvent = values[i - 1] + range * (values[i - 1] - values[0]);

    if (previousEvent + nextEvent <= currentTime) {
      previousEvent = currentTime;
      return previousEvent;
    }
    assert previousEvent + nextEvent > currentTime; // inline assert generated by ARG-V
    return previousEvent + nextEvent;
  }

  /** This main was generated by ARG-V */
  public static void main(String[] args) throws Exception {
    Main instance = new Main();
    instance.generateNextEvent(Verifier.nondetDouble());
  }
}
