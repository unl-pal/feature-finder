import org.sosy_lab.sv_benchmarks.Verifier;

/** filtered by ARG-V */
public class Main {

  /** ARG-V: suitable */
  public static double asin(double v) {
    if (Math.abs(v) > 1.) return v < 0.0 ? -Math.PI / 2 : Math.PI / 2;
    return Math.asin(v);
  }

  public static double acos(double v) {
    if (Math.abs(v) > 1.) return v < 0.0 ? Math.PI : 0.0;
    return Math.acos(v);
  }

  /** ARG-V: suitable */
  public static double hypot(double x, double y) {
    if (x < 0.0) {
      x = -x;
    } else if (x == 0.0) return y < 0.0 ? -y : y;
    if (y < 0.0) {
      y = -y;
    } else if (y == 0.0) return x;
    if (x < y) {
      x /= y;
      return y * Math.sqrt(1.0 + x * x);
    } else {
      y /= x;
      return x * Math.sqrt(1.0 + y * y);
    }
  }

  // For negative angles, d should be negative, m & s positive.
  /** ARG-V: suitable */
  public static double dmsToRad(double d, double m, double s) {
    if (d >= 0) return (d + m / 60 + s / 3600) * Math.PI / 180.0;
    return (d - m / 60 - s / 3600) * Math.PI / 180.0;
  }

  // For negative angles, d should be negative, m & s positive.
  /** ARG-V: suitable */
  public static double dmsToDeg(double d, double m, double s) {
    if (d >= 0) return (d + m / 60 + s / 3600);
    return (d - m / 60 - s / 3600);
  }

  /** ARG-V: suitable */
  public static double takeSign(double a, double b) {
    a = Math.abs(a);
    if (b < 0) return -a;
    return a;
  }

  /** ARG-V: suitable */
  public static double qsfn(double sinphi, double e, double one_es) {
    double con = Verifier.nondetDouble();

    if (e >= 1.0e-7) {
      con = e * sinphi;
      return (one_es * (sinphi / (1. - con * con) - (.5 / e) * Math.log((1. - con) / (1. + con))));
    } else return (sinphi + sinphi);
  }

  /*
   * Java translation of "Nice Numbers for Graph Labels"
   * by Paul Heckbert
   * from "Graphics Gems", Academic Press, 1990
   */
  /** ARG-V: suitable */
  public static double niceNumber(double x, boolean round) {
    int expv = Verifier.nondetInt(); /* exponent of x */
    double f = Verifier.nondetDouble(); /* fractional part of x */
    double nf = Verifier.nondetDouble(); /* nice, rounded fraction */

    expv = (int) Math.floor(Math.log(x) / Math.log(10));
    f = x / Math.pow(10., expv); /* between 1 and 10 */
    if (round) {
      if (f < 1.5) {
        nf = 1.;
      } else if (f < 3.) {
        nf = 2.;
      } else if (f < 7.) {
        nf = 5.;
      } else nf = 10.;
    } else if (f <= 1.) {
      nf = 1.;
    } else if (f <= 2.) {
      nf = 2.;
    } else if (f <= 5.) {
      nf = 5.;
    } else nf = 10.;
    return nf * Math.pow(10., expv);
  }

  /** This main was generated by ARG-V */
  public static void main(String[] args) throws Exception {
    asin(Verifier.nondetDouble());
    acos(Verifier.nondetDouble());
    hypot(Verifier.nondetDouble(), Verifier.nondetDouble());
    dmsToRad(Verifier.nondetDouble(), Verifier.nondetDouble(), Verifier.nondetDouble());
    dmsToDeg(Verifier.nondetDouble(), Verifier.nondetDouble(), Verifier.nondetDouble());
    takeSign(Verifier.nondetDouble(), Verifier.nondetDouble());
    qsfn(Verifier.nondetDouble(), Verifier.nondetDouble(), Verifier.nondetDouble());
    niceNumber(Verifier.nondetDouble(), Verifier.nondetBoolean());
  }
}
