  /**
   * Calculates the Sun's true longitude in degrees. The result is an angle gte 0 and lt 360.
   * Requires the Sun's mean anomaly, also in degrees
   *
   * @param sunMeanAnomaly
   * @return
   */
  /** ARG-V: suitable */
  private static double getSunTrueLongitude(double sunMeanAnomaly) {
    double l = Verifier.nondetDouble();
    Verifier.assume(l == l); // ensures that l is not NaN
    // get longitude into 0-360 degree range
    if (l >= 360.0) {
      l = 360.0;
    }
    if (l < 0) {
      l = 360.0;
    }
    assert l <= 360.0 && l >= 0.0; // inline assert generated by ARG-V
    return l + sunMeanAnomaly;
  }

